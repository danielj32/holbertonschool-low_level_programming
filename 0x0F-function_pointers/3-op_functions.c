#include "3-calc.h"
/**
 *op_add - ss
 *@b: int
 *@c: int
 *Return: 0
 */

int op_add(int b, int c)
{
	return (b + c);
}
/**
 *op_sub - fd
*@b: int
*@c: int
 *Return: 0
 */
int op_sub(int b, int c)
{
	return (b - c);
}
/**
 *op_mul - ss
 *@b: int
 *@c: int
 *Return: 0
 */
int op_mul(int b, int c)
{
	return (b * c);
}
/**
 *op_div - ssd
 *@b: int
 *@c: int
 *Return: 0
 */

int op_div(int b, int c)
{
	return (b / c);
}
/**
 *op_mod - sdd
 *@b: int
 *@c: int
 *Return: 0
 */
int op_mod(int b, int c)
{
	return (b % c);
}
